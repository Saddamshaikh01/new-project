
package program;

/**
 * Write a program to print 1 10 2 9 3 8 4 7 5 6
 * For print this pattern number we use for loop
 * We have to take two variable i and j and intialize in loop and then add condition
 * And then add increment and decreament operator and then it will print our output
 *
 */
public class Prnt_Number {

	public static void main(String[] args) {
		for(int i=1, j=10; i<=5;i++,j--) {
			System.out.print(i+" ");
			System.out.print(j+" ");
		}
		

	}

}
